    private void VenderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_VenderMouseClicked

       
        String Vendidos;
        int cantidad;
        

        if (this.Prod.isEmpty() == false) {

            try {
                int Valor = 0;
                short Cantidad = 0;
                String Descripcion = "";

                for (int k = 0; k < this.Prod.size(); k++) {
                    Valor = Valor + (Integer.parseInt(this.Prod.get(k).getPrecioDeVenta()) * Integer.parseInt(this.Prod.get(k).getCantidad()));
                    Cantidad = (short) (Cantidad + Double.parseDouble(this.Prod.get(k).getCantidad()));
                    Descripcion = Descripcion +"\n"+ "Nombre:"+this.Prod.get(k).getNombre() 
                            +"\n"+"Cantidad:"+this.Prod.get(k).getCantidad() +"\n";

                    Vendidos = (short) (Integer.parseInt(this.MetodosDB.consultarpor(6, 2, Prod.get(k).getNombre())) + Integer.parseInt(this.Prod.get(k).getCantidad())) + "";
                    this.MetodosDB.EditarPor(7, Vendidos, 2, this.Prod.get(k).getNombre());

                    cantidad = Integer.parseInt(this.MetodosDB.consultarpor(5, 2, this.Prod.get(k).getNombre())) - Integer.parseInt(this.Prod.get(k).getCantidad());
                    System.out.println(cantidad);
                    this.MetodosDB.EditarPor(5, cantidad + "", 2, this.Prod.get(k).getNombre());
                }
                
                this.PonerEnBlancoTxt();
                this.Errordatosincorrectos.setVisible(false);
                this.DineroRecolectar();
                
                Factura fact = new Factura(new FacturaClase(Valor + "", Cantidad + "", Descripcion + "",this.Prod));
                fact.setVisible(true);
                this.dispose();

            } catch (Exception e) {
                System.out.println("Error en insertar2 " + e);
                this.Errordatosincorrectos.setVisible(true);
            }
        } else {
            this.Errordatosincorrectos.setVisible(true);
        }
    }//GEN-LAST:event_VenderMouseClicked

