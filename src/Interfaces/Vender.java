    public Double DineroRecolectar() throws SQLException{
        double din=0;
        for(int k=0;k<this.Prod.size();k++){
            din = din + (Double.parseDouble(this.Prod.get(k).getPrecioDeVenta())
                    * Double.parseDouble(this.Prod.get(k).getCantidad()));
        }
        this.MetodosDB.editarDinero(din);
        System.out.println("EL DINERO ES"+din);
        return din;
    }

