    private void btnagregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnagregarMouseClicked

        try {

            if (this.txtNombre.getText().isEmpty() == false
                    && this.MetodosDB.ExisteoNO(txtNombre.getText()) == true
                    && Integer.parseInt(this.txtCantidad.getText()) <= Integer.parseInt(this.MetodosDB.ConsultarVender(this.txtNombre.getText()))
                    && Integer.parseInt(this.MetodosDB.ConsultarVender(txtNombre.getText())) > 0) {

                Productos prod = this.MetodosDB.consultartodoporResultSetVender( this.txtNombre.getText());
                prod.setCantidad(this.txtCantidad.getText());
                this.Prod.add(prod);
                this.lablista.setText(this.lablista.getText() + prod.getNombre() + ",");
                this.PonerEnBlancoTxt();

            } else {
                this.Errordatosincorrectos.setVisible(true);
            }
        } catch (Exception e) {
            System.out.println("Error en insertar2 " + e);
            this.Errordatosincorrectos.setVisible(true);
        }


    }//GEN-LAST:event_btnagregarMouseClicked

