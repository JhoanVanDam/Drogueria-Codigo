    private void AgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AgregarMouseClicked
        this.db.conexion();
        String Nombre, Costo, Venta, Cantidad, FechaCaducidad, Ubicacion;
        Nombre = this.txtNombre.getText();
        Costo = this.txtCosto.getText();
        Venta = this.txtValor.getText();
        Cantidad = this.txtCantidad.getText();
        FechaCaducidad = this.txtFecha.getText();
        Ubicacion = this.txtUbicacion.getText();
        try {

            if (Costo.isEmpty() == false
                    && Venta.isEmpty() == false
                    && Cantidad.isEmpty() == false
                    && Nombre.isEmpty() == false) {

                this.db.insertar("INSERT INTO Productos (prod_id,prod_descripcion,prod_costo,prod_venta,prod_cantidad,"
                        + "ventas_desde,prod_fechavencimiento,prod_ubicacion)values"
                        + "(null" + "," + "'" + Nombre + "'" + "," + "'" + Costo + "'" + "," + "'"
                        + Venta + "'" + "," + "'" + Cantidad + "'" + ",0,'" + FechaCaducidad + "','" + Ubicacion + "'" + ");");
//            System.out.println("INSERT INTO Productos (prod_id,prod_descripcion,prod_costo,prod_venta,prod_cantidad,"
//                    + "ventas_desde,prod_fechavencimiento)values"
//                    + "(null" + "," + "'" + Nombre + "'" + "," + "'" + Costo + "'" + "," + "'"
//                    + Venta + "'" + "," + "'" + Cantidad + "'" + ",0,'" +FechaCaducidad.toString()+ "');");

                this.PonerEnBlancoTxt();
                this.Errordatosincorrectos.setVisible(false);

            } else {
                this.Errordatosincorrectos.setVisible(true);
            }
        } catch (Exception e) {
            System.out.println("Error en insertar2 " + e);
            this.Errordatosincorrectos.setVisible(true);
        }
    }//GEN-LAST:event_AgregarMouseClicked

